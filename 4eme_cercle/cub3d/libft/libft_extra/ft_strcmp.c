#include "../libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	if (s1 && !s2)
		return ((unsigned char)s1[0]);
	if (!s1 && s2)
		return (-(unsigned char)s2[0]);
	if (!s1 && !s2)
		return (0);
	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
