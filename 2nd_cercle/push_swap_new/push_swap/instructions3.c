#include "push_swap.h"

void	ft_rrr(t_lst **a, t_lst **b)
{
	if (*a)
	{
		*a = ft_rra(*a, *b, "");
		*a = (*a)->first;
	}
	if (*b)
	{
		*b = ft_rrb(*a, *b, "");
		*b = (*b)->first;
	}
	ft_print_str(*a, "rrr\n");
}
